# doel van het programma  het vraagt namen en maakt van die namen lootjes die worden gekopeld aan de namen
# namen = een lijst om hier worden de namen in opgeslagen

# er word gevraagt om 3 verschillende namen van de spelers
# als er een naam die al ingevoert is nog een keer word ingevoert dan zegt het programma dat die al gekozen is
# na 3 namen vraagt het programma of je nog een naam wilt invoeren
# als het antwoord ja is dan word er nog een keer gevraagt om een naam

# als het antwoord nee is dan worden van de namen lootjes gemaakt

# die lootjes worden allemaal geschuffeld
# de geschuffelde lootjes worden gekkopeld aan de namen
# als de namen gekkopeld worden aan een lootje van zichzelf
# dan worden de lootjes opnieuw geschuffled en worden de namen opnieuw gekkopeld aan de lootjes

# als de namen niet gekkopeld worden aan een lootje van zichzelf
# dan worden alle lootjes geprint en wie het getrokken heeft